const SLS_REGION = process.env.SLS_REGION;
const TASKER_PROJECT_TABLE_NAME = process.env.TASKER_PROJECT_TABLE_NAME || "";
const TASKER_TASK_TABLE_NAME = process.env.TASKER_TASK_TABLE_NAME || "";
const TASKER_USER_TABLE_NAME = process.env.TASKER_USER_TABLE_NAME || "";
const TASKER_TASK_EXTRA_TABLE_NAME =
  process.env.TASKER_TASK_EXTRA_TABLE_NAME || "";
const TASKER_TEAM_TABLE_NAME = process.env.TASKER_TEAM_TABLE_NAME || "";

const client = new DynamoDBClient({ region: SLS_REGION });
const docClient = DynamoDBDocument.from(client);

export const handler = async () => {
  try {
    // Insert data into DynamoDB tables
    for (const [tableName, items] of Object.entries(seedData)) {
      console.info(
        `Populating data for table ${tableName} with ${items.length} items...`
      );
      let dynamoTableName = "";

      // Map seed data keys to corresponding DynamoDB table names
      switch (tableName) {
        case "projects":
          dynamoTableName = TASKER_PROJECT_TABLE_NAME;
          break;
        case "tasks":
          dynamoTableName = TASKER_TASK_TABLE_NAME;
          break;
        case "users":
          dynamoTableName = TASKER_USER_TABLE_NAME;
          break;
        case "attachments":
          dynamoTableName = TASKER_TASK_EXTRA_TABLE_NAME;
          break;
        case "comments":
          dynamoTableName = TASKER_TASK_EXTRA_TABLE_NAME;
          break;
        case "teams":
          dynamoTableName = TASKER_TEAM_TABLE_NAME;
          break;
        default:
          console.warn(`Unknown table name: ${tableName}`);
          continue;
      }

      for (const item of items as Array<any>) {
        const params: PutCommandInput = {
          TableName: dynamoTableName,
          Item: item,
        };

        try {
          if (!DRY_RUN) {
            await docClient.put(params);
          }
        } catch (err) {
          console.error(`Failed to insert item into ${dynamoTableName}:`, err);
        }
      }
      console.info(`Data population for table ${tableName} complete.`);
    }

    console.log("Data population complete.");
  } catch (error) {
    console.error("Failed to populate data:", error);
  }
};
